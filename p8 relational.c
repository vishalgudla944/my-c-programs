#include<stdio.h>
int main()
{
    int a=10,b=2,c=10;
   printf ("\na>b is %d",(a>b));
   printf ("\na>c is %d",(a>c));

   printf ("\na<b is %d",(a<b));
   printf ("\na<c is %d",(a<c));

   printf ("\na>=b is %d",(a>=b));
   printf ("\na>=c is %d",(a>=c));

   printf ("\na<=b is %d",(a<=b));
   printf ("\na<=c is %d",(a<=c));

   printf ("\na==b is %d",(a==b));
   printf ("\na==c is %d",(a==c));

   printf ("\na!=b is %d",(a!=b));
   printf ("\na!=c is %d\n",(a!=c));

   return 0;


}
