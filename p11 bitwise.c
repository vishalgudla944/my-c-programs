#include<stdio.h>
int main()
{
    int a=5, b=7;

    /*bitwise And*/
    printf("a&b is %d",(a&b));
    /*bitwise inclusive or*/
    printf("\na|b is %d",(a|b));
    /*bitwise exclusive or*/
    printf("\na^b is %d",(a^b));
    /*bitwise leftshit*/
    printf("\na<<2 is %d",(a<<2));
    /*bitwise rightshift*/
    printf("\na>>1 is %d",(a>>1));
    return 0;


}
